  function applyLang(lang) {
    if (!T[lang]) return;
    document.documentElement.setAttribute('lang', lang);
    [].forEach.call(document.querySelectorAll('[data-i18n]'), function (el) {
      var key = el.getAttribute('data-i18n');
      if (T[lang][key] !== undefined) el.textContent = T[lang][key];
    });
    [].forEach.call(document.querySelectorAll('[data-i18n-html]'), function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (T[lang][key] !== undefined) el.innerHTML = T[lang][key];
    });
    [].forEach.call(document.querySelectorAll('.lang-btn'), function (btn) {
      var active = btn.getAttribute('data-lang') === lang;
      btn.classList.toggle('active', active);
      btn.setAttribute('aria-pressed', active ? 'true' : 'false');
    });
    try { localStorage.setItem('aroma_lang', lang); } catch (e) {}
  }

  function init() {
    var saved;
    try { saved = localStorage.getItem('aroma_lang'); } catch (e) {}
    applyLang(saved && T[saved] ? saved : 'nl');
    [].forEach.call(document.querySelectorAll('.lang-btn'), function (btn) {
      btn.addEventListener('click', function () { applyLang(btn.getAttribute('data-lang')); });
    });
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
